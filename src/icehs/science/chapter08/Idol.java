package icehs.science.chapter08;

public class Idol {
		private String name;
		private String group;
		
		public Idol(String group, String name) {
			this.name = name;
			this.group = group;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		public String getGroup() {
			return group;
		}

		public void setGroup(String group) {
			this.group = group;
		}
		
		

	

}
